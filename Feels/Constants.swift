import Foundation
import UIKit

extension UIColor {
    
    static func primary() -> UIColor {
        return UIColor(red: 0.4824, green: 0.1216, blue: 0.6353, alpha: 1.0) 
    }
    
    static func basavaRed() -> UIColor {
        return UIColor(red: 0.9373, green: 0.2353, blue: 0.349, alpha: 1.0)
    }
}
